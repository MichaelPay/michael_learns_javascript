// Calling items in an array by index number

var shopping = [ 'carrots', 'milk', 'eggs' ];
console.log(shopping[0]);
console.log(shopping[1]);
console.log(shopping[2]);

// Push method of an array

shopping.push('ham');
console.log(shopping[3]);

// Unshift method - adding item to beginning of array

shopping.unshift('apples');
console.log(shopping[0]);



